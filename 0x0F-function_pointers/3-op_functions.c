#include "3-calc.h"
/**
 * op_add - function for addition
 * @a: first integer
 * @b: second integer
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function for subraction
 * @a: first int
 * @b: second int
 * Return: subraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function for multiplication
 * @a: first int
 * @b: second int
 * Return: multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division function
 * @a: first int
 * @b: second int
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulation function
 * @a: forst int
 * @b: second int
 * Return: modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}
